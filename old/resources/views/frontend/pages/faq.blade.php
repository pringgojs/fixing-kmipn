        <h4><b>Umum</b></h4>
        <ol>
          <li><b>Bagaimana pendaftaran KMIPN 2018?</b></li>
          <ul>
            <li>
              Pendaftaran dilakukan 2 tahap (wajib) yaitu oleh Penanggung jawab PT dan Ketua Tim. Pendaftaran PT akan mempengaruhi daftar PT untuk pendaftaran Tim. Jadi harus dilakukan berurut.
            </li>
          </ul>
          <li><b>Apakah manfaat pendaftaran PT</b></li>
          <ul>
            <li>
              Untuk memberikan estimasi ataupun data sebenarnya terkait jumlah tim yang berpartisipasi di KMIPN 2018 dari PT terkait. Kedepannya akan ditambahkan fitur untuk melihat rekapitulasi tim yang mewakili PT terkait beserta status verifikasi tim.
            </li>
          </ul>
          <li><b>Apakah ada notifikasi atau verifikasi sistem pada saat melakukan pendaftaran KMIPN 2018?</b></li>
          <ul>
            <li>
              Tidak ada notifikasi dari sistem saat proses pertama kali pendaftaran. Namun dapat dilihat pada status akun Anda setelah mendaftar apakah "sudah terverifikasi" atau "belum terverifikasi". Notifikasi akan diterima bila akun sudah terverifikasi.
            </li>
          </ul>
        </ol>
        <h4><b>Tentang Politeknik</b></h4>
        <ol>
          <li><b>Bagaimana jika status PT belum terverifikasi?</b></li>
          <ul>
            <li>
              Verifikasi dilakukan secara bertahap dengan memeriksa kebenaran data yang didaftarkan. Silahkan menghubungi Panitia KMIPN (kmipn@cs.pens.ac.id) jika masih belum ada progress hingga H-5 sebelum batas pendaftaran Tim
            </li>
          </ul>
          <li>
            <b>Bagaimana jika Nama PT sudah ada di daftar PT yang telah melakukan pendaftaran. Padahal kami dari PT belum melakukan pendaftaran?</b>
          </li>
          Hal ini memang kami perbolehkan untuk mempercepat dan mempermudah bagi mahasiswa untuk melakukan pendaftaran tim. <br> Lalu bagaimana:
          <ul>
            <li>
              Seharusnya mahasiswa/pihak yang melakukan pendaftaran PT padahal bukan haknya atau bukan sebagai Penanggung Jawab/Ketua Kontingen, dapat melakukan informasi ke PT (yang berwanang).
            </li>
            <li>
              Jika tidak kenal dan tidak mengetahui keberadaan yang mendaftaran PT/pihak berwenang. Perwakilan PT (Penanggung jawab/Ketua Kontingen) dapat melakukan pemindah-tanganan akun PT tsb dengan menginformasikan ke Panitia KMIPN 2018 (kmipn@cs.pens.ac.id) dengan melampirkan Nama Penanggun Jawab/Ketua Kontingen (baru), Email (baru) yang wajib menggunakan alamat email berdomain PT. Selanjutnya Panitia akan melakukan reset data PT.
            </li>
          </ul>
          <li>
            <b>Apa yang terjadi apabila ada permintaan untuk menghapus PT karena bukan perwakilan PT yang melakukan pendaftaran?</b>
          </li>
          <ul>
            <li>Penghapusan PT akan berakibat hilangnya semua data Tim yang telah melakukan pendaftaran dengan menggunakan PT tersebut.</li>
          </ul>
        </ol>
        <h4><b>Tentang TIM</b></h4>
        <ol>
          <li>
            <b>Kapan verifikasi TIM?</b>
            <ul>
              <li>Verifikasi dilakukan bertahap oleh Panitia dengan melakukan cek data TIM. Dilakukan secara manual. Mohon dapat menunggu.</li>
              <li>Status tim yang telah diverifikasi akan menerima notifikasi dari sistem.</li>
            </ul>
          </li>
          <li>
            <b>Saya dapat info dari PT untuk melakukan pendaftaran ulang TIM, tapi mengapa alamat email saya tidak bisa dipakai? Alasan sistem karena sudah ada yang pakai.</b>
            <ul>
              <li>Hal ini terjadi karena admin PT (baru) sepakat untuk menghapus admin PT (lama) sehingga semua TIM yang mendaftar dengan admin PT (lama) akan ikut terhapus. Silahkan mencoba lagi saja karena sistem menghilangkan data tsb dilakukan dengan penghapusan daftar TIM dahulu baru menghapus PT.</li>
            </ul>
          </li>
          <li>
            <b>Bagaimana cara mengupdate data TIM?</b>
            <ul>
              <li>Data Tim hanya dapat diubah ketika data belum diapprove atau diverifikasi, untuk menghindari kecurangan</li>
            </ul>
          </li>
          <li>
            <b>Bagaimana bila tidak ada KTM (Kartu Tanda Mahasiswa)?</b>
            <ul>
              <li>Bisa digantikan dengan Surat Keterangan Mahasiswa</li>
            </ul>
          </li>
        </ol>
